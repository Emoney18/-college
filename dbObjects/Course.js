class Course{
    constructor(
        courseId,
        department,
        name,
        uniqueID,
        unit,
        code,
        CourseLevel,
        status,
    ){
        this.CourseId = courseId;
        this.DepartmentId = DepartmentId;
        this.Name = name;
        this.UniqueID = uniqueID;
        this.Unit = unit;
        this.Code = code;
        this.CourseLevel = CourseLevel;
        this.Status = status;
    }
}