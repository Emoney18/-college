class Lecturer{
    constructor(
        lecturerId,
        department,
        surName,
        firstName,
        OtherName,
        staffId,
        status,
    ){
        this.LecturerId = lecturerId;
        this.Department = department;
        this.SurName = surName;
        this.FirstName = firstName;
        this.OtherName = OtherName;
        this.StaffId = staffId;
        this.Status = status;
    }
}